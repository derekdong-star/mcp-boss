@mcp.tool()
async def login_start_interactive(ctx: Context) -> str:
    """Interactive login flow: guide user through scan and confirmation"""
    try:
        await ctx.info("Starting interactive login flow")

        while True:  # Outer loop for full login retry
            while True:  # Inner loop for QR regeneration
                # Step 1: Start login and generate QR code
                session = state.get_session()
                qr_id = await BossZhipinAPI.get_randkey(session)
                state.update_login_status(qr_id=qr_id, login_step="qr_generated")

                # Get QR code
                qr_image_data = await BossZhipinAPI.get_qrcode(session, qr_id)

                # Save QR code image
                filename = f"qrcode_{qr_id}.png"
                filepath = state.static_dir / filename

                with open(filepath, "wb") as f:
                    f.write(qr_image_data)

                # Generate image URL
                image_url = f"http://127.0.0.1:8000/static/{filename}"
                state.update_login_status(image_url=image_url)

                # Display QR code info
                await ctx.info("=" * 50)
                await ctx.info("BossZhipin login QR code generated!")
                await ctx.info(f"QR Image URL: {image_url}")
                await ctx.info(f"QR ID: {qr_id}")
                await ctx.info("=" * 50)

                # Step 2: Ask user if scanned
                scan_result = await ctx.elicit(
                    "Please scan the QR code with BossZhipin APP, then select 'Scanned'",
                    response_type=["Scanned", "Regenerate QR", "Cancel Login"]
                )

                if scan_result.action != "accept" or scan_result.data not in ["Scanned", "Regenerate QR"]:
                    return json.dumps({
                        "status": "cancelled",
                        "message": "User cancelled login"
                    }, ensure_ascii=False, indent=2)

                if scan_result.data == "Regenerate QR":
                    await ctx.info("Regenerating QR code...")
                    state.reset_login()
                    continue

                # Step 3: Check scan status
                await ctx.info("Verifying scan status...")
                status_code = await BossZhipinAPI.check_scan_status(session, qr_id)

                if status_code == 200:
                    scan_check = {"status": "scanned"}
                elif status_code == 409:
                    scan_check = {"status": "waiting"}
                else:
                    scan_check = {"status": "error", "message": f"Unknown status: {status_code}"}

                if scan_check["status"] != "scanned":
                    await ctx.warning("Scan not detected, please confirm if scan was successful")
                    retry_result = await ctx.elicit(
                        "Rescan?",
                        response_type=["Rescan", "Continue Waiting", "Cancel Login"]
                    )

                    if retry_result.action != "accept" or retry_result.data == "Cancel Login":
                        return json.dumps({
                            "status": "cancelled",
                            "message": "User cancelled login"
                        }, ensure_ascii=False, indent=2)

                    if retry_result.data == "Rescan":
                        state.reset_login()
                        continue

                # Scan successful, exit inner loop
                break

            # Step 4: Wait for user confirmation on phone
            await ctx.info("Please confirm login on BossZhipin APP...")

            wait_messages = [
                "Waiting for confirmation...",
                "Waiting for user confirmation...",
                "Please tap confirm on your phone...",
                "Waiting for login confirmation..."
            ]

            login_success = False
            for i in range(60):  # Max 60 seconds wait
                message = wait_messages[i % len(wait_messages)]
                await ctx.info(f"{message} ({i+1}/60s)")

                # Check login confirmation status
                confirm_status_code = await BossZhipinAPI.check_login_confirmation(session, qr_id)

                if confirm_status_code == 200:
                    # Get final cookie
                    cookie_str, bst_value = await BossZhipinAPI.get_final_cookie(session, qr_id)

                    state.update_login_status(
                        is_logged_in=True,
                        cookie=cookie_str,
                        bst=bst_value,
                        login_step="logged_in"
                    )

                    confirm_result = {
                        "status": "logged_in",
                        "message": "Login successful!",
                        "has_cookie": True,
                        "has_bst": True,
                        "login_step": "logged_in"
                    }

                    await ctx.info("Login successful!")
                    return json.dumps(confirm_result, ensure_ascii=False, indent=2)
                elif confirm_status_code == 409:
                    await asyncio.sleep(1)
                    continue
                elif confirm_status_code == 408:
                    confirm_result = {"status": "timeout", "message": "Login confirmation timeout"}
                else:
                    confirm_result = {"status": "error", "message": f"Unknown confirmation status: {confirm_status_code}"}

                if confirm_result["status"] in ["timeout", "error"]:
                    await ctx.error(f"Login failed: {confirm_result['message']}")

                    retry_result = await ctx.elicit(
                        f"Login failed: {confirm_result['message']}. Restart login?",
                        response_type=["Restart Login", "Cancel"]
                    )

                    if retry_result.action == "accept" and retry_result.data == "Restart Login":
                        state.reset_login()
                        await ctx.info("Restarting login flow...")
                        break  # Exit current confirmation wait, restart outer loop
                    else:
                        return json.dumps({
                            "status": "cancelled",
                            "message": "User chose not to retry login"
                        }, ensure_ascii=False, indent=2)

            # If reached here, login timed out
            if not login_success:
                await ctx.warning("Confirmation timeout")
                timeout_result = await ctx.elicit(
                    "Confirmation timeout, restart login?",
                    response_type=["Restart Login", "Cancel"]
                )

                if timeout_result.action == "accept" and timeout_result.data == "Restart Login":
                    state.reset_login()
                    await ctx.info("Restarting login flow...")
                    continue  # Restart entire process
                else:
                    return json.dumps({
                        "status": "timeout",
                        "message": "Login timeout, user chose not to retry"
                    }, ensure_ascii=False, indent=2)

    except Exception as e:
        error_msg = f"Interactive login failed: {str(e)}"
        await ctx.error(error_msg)
        return json.dumps({
            "status": "error",
            "message": error_msg
        }, ensure_ascii=False, indent=2)


