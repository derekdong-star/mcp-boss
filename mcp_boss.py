def background_scan_monitor(qr_id: str):
    """Monitor scan and confirmation status in a background thread"""
    try:
        session = state.get_session()
        scan_url = f"https://www.zhipin.com/wapi/zppassport/qrcode/scan?uuid={qr_id}"
        confirm_url = f"https://www.zhipin.com/wapi/zppassport/qrcode/scanLogin?qrId={qr_id}&status=1"

        # Phase 1: Wait for scan (long polling)
        print(f"[Monitor] Starting scan monitoring, QR ID: {qr_id}")
        scan_count = 0
        while True:
            if state.login_status.login_step == "logged_in":
                print("[Monitor] Already logged in, stopping monitor")
                return

            try:
                scan_count += 1
                print(f"[Monitor] Scan check #{scan_count}")
                resp = session.get(scan_url, timeout=35)

                if resp.status_code == 200:
                    json_data = resp.json()
                    if json_data.get("scaned"):
                        print("[Monitor] Scan detected, entering confirmation phase")
                        state.update_login_status(login_step="scanned")
                        break
                    elif json_data.get("msg") == "timeout":
                        print(f"[Monitor] Scan timeout, continuing poll... ({scan_count})")
                    else:
                        print(f"[Monitor] Polling... ({scan_count}) - {json_data}")
                else:
                    print(f"[Monitor] Unknown scan status: {resp.status_code}")

            except requests.exceptions.ReadTimeout:
                print(f"[Monitor] Scan read timeout, continuing... ({scan_count})")
                continue
            except Exception as e:
                print(f"[Monitor] Scan check error: {e}")
                time.sleep(2)

            time.sleep(1)

        # Phase 2: Wait for confirmation (long polling)
        if state.login_status.login_step == "scanned":
            print("[Monitor] Starting confirmation monitoring")
            confirm_count = 0
            while True:
                try:
                    confirm_count += 1
                    print(f"[Monitor] Confirm check #{confirm_count}")
                    resp = session.get(confirm_url, timeout=35)

                    if resp.status_code == 200:
                        print("[Monitor] User confirmed login, retrieving cookies")

                        # Generate fp parameter
                        i_str = "8048b8676fb7d3d8952276e6e98e0bde.f2dc7a63c4b0fbfa4b51a07e2710cf83.fef7e750fc3a1e6327e8a880915aee9c.ae00f848beb1aa591d71d5a80dd3bd95"
                        e_b64 = "clRwXUJBK1VKK0k0IWFbbQ=="

                        key_bytes = base64.b64decode(e_b64)
                        plaintext_bytes = i_str.encode('utf-8')
                        iv_bytes = get_random_bytes(16)
                        cipher = AES.new(key_bytes, AES.MODE_CBC, iv_bytes)
                        padded_plaintext = pad(plaintext_bytes, AES.block_size)
                        ciphertext_bytes = cipher.encrypt(padded_plaintext)
                        result_bytes = iv_bytes + ciphertext_bytes
                        fp = base64.b64encode(result_bytes).decode('utf-8')

                        dispatcher_url = f"https://www.zhipin.com/wapi/zppassport/qrcode/dispatcher?qrId={qr_id}&pk=header-login&fp={fp}"
                        cookie_resp = session.get(dispatcher_url, allow_redirects=False)

                        # Parse Set-Cookie headers
                        set_cookie_headers = cookie_resp.headers.get('Set-Cookie', '')
                        cookie_str = ''
                        bst_value = ''

                        if set_cookie_headers:
                            cookies = {}
                            cookie_parts = set_cookie_headers.split(',')
                            for part in cookie_parts:
                                if '=' in part:
                                    name_value = part.strip().split(';')[0].strip()
                                    if '=' in name_value:
                                        name, value = name_value.split('=', 1)
                                        cookies[name.strip()] = value.strip()

                            cookie_str = '; '.join([f"{k}={v}" for k, v in cookies.items()])
                            if 'bst' in cookies:
                                bst_value = cookies['bst']

                        # Phase 3: Complete security check with headless browser
                        print("[Monitor] Starting security check...")
                        state.update_login_status(login_step="security_check")

                        try:
                            loop = asyncio.new_event_loop()
                            asyncio.set_event_loop(loop)
                            final_cookie_str = loop.run_until_complete(
                                BossZhipinAPI.complete_security_check(cookie_str)
                            )
                            loop.close()

                            print("[Monitor] Security check completed")

                            # Update session with final cookies
                            for cookie_pair in final_cookie_str.split('; '):
                                if '=' in cookie_pair:
                                    name, value = cookie_pair.split('=', 1)
                                    session.cookies.set(name, value)

                            state.update_login_status(
                                is_logged_in=True,
                                cookie=final_cookie_str,
                                bst=bst_value,
                                login_step="logged_in"
                            )

                            print("[Monitor] Login successful! Final cookie saved")

                        except Exception as e:
                            print(f"[Monitor] Security check failed, using initial cookie: {e}")

                            if cookie_str:
                                for cookie_pair in cookie_str.split('; '):
                                    if '=' in cookie_pair:
                                        name, value = cookie_pair.split('=', 1)
                                        session.cookies.set(name, value)

                            state.update_login_status(
                                is_logged_in=True,
                                cookie=cookie_str,
                                bst=bst_value,
                                login_step="logged_in"
                            )

                            print("[Monitor] Login successful! Initial cookie saved")

                        return
                    else:
                        json_data = resp.json()
                        if json_data.get("msg") == "timeout":
                            print(f"[Monitor] Confirm timeout, continuing... ({confirm_count})")
                        else:
                            print(f"[Monitor] Polling... ({confirm_count}) - {json_data}")

                except requests.exceptions.ReadTimeout:
                    print(f"[Monitor] Confirm read timeout, continuing... ({confirm_count})")
                    continue
                except Exception as e:
                    print(f"[Monitor] Confirm check error: {e}")
                    time.sleep(2)

                time.sleep(1)

    except Exception as e:
        print(f"[Monitor] Monitor thread error: {e}")
    finally:
        print("[Monitor] Monitor thread ended")


class BossZhipinAPI:
    """BossZhipin API operations class"""

    @staticmethod
    def generate_fp(i_str: str, e_b64: str) -> str:
        """Generate device fingerprint parameter"""
        key_bytes = base64.b64decode(e_b64)
        plaintext_bytes = i_str.encode('utf-8')
        iv_bytes = get_random_bytes(16)

        cipher = AES.new(key_bytes, AES.MODE_CBC, iv_bytes)
        padded_plaintext = pad(plaintext_bytes, AES.block_size)
        ciphertext_bytes = cipher.encrypt(padded_plaintext)

        result_bytes = iv_bytes + ciphertext_bytes
        return base64.b64encode(result_bytes).decode('utf-8')

    @staticmethod
    async def complete_security_check(initial_cookie: str) -> str:
        """Complete security check using headless browser to get final cookie with __zp_stoken__

        Args:
            initial_cookie: Initial cookie from dispatcher API

        Returns:
            Final cookie string containing __zp_stoken__
        """
        security_check_url = (
            "https://www.zhipin.com/web/common/security-check.html?"
            "seed=ttttZij2JIIK%2BxUw73%2B6ZmzsaYKTbDQuIH6OR6Bm54o%3D"
            "&name=e331459e"
            "&ts=1762256958405"
            "&callbackUrl=https%3A%2F%2Fwww.zhipin.com%2Fweb%2Fgeek%2Fjobs"
        )

        print("[Security] Starting security check with headless browser")

        try:
            async with async_playwright() as p:
                browser = await p.chromium.launch(headless=True)
                context = await browser.new_context()

                # Parse and set initial cookies
                cookies = []
                for cookie_pair in initial_cookie.split('; '):
                    if '=' in cookie_pair:
                        name, value = cookie_pair.split('=', 1)
                        cookies.append({
                            'name': name,
                            'value': value,
                            'domain': '.zhipin.com',
                            'path': '/'
                        })

                await context.add_cookies(cookies)
                print(f"[Security] Set {len(cookies)} initial cookies")

                # Access security-check page
                page = await context.new_page()
                print("[Security] Accessing security-check page...")
                await page.goto(security_check_url, wait_until='domcontentloaded')

                # Wait for network idle
                print("[Security] Waiting for network idle...")
                try:
                    await page.wait_for_load_state('networkidle', timeout=30000)
                    print("[Security] Network idle reached")
                except Exception as e:
                    print(f"[Security] Network idle timeout: {e}")

                # Additional wait for JS execution
                print("[Security] Waiting additional 3s for cookies to be set...")
                await asyncio.sleep(3)

                # Get cookies via JavaScript
                print("[Security] Reading cookies via JavaScript...")
                js_cookies = await page.evaluate("() => document.cookie")
                print(f"[Security] JS cookies: {js_cookies[:200]}...")

                # Get cookies via Playwright API
                print("[Security] Getting cookies via Playwright API...")
                final_cookies = await context.cookies()
                playwright_cookie_str = '; '.join([f"{c['name']}={c['value']}" for c in final_cookies])
                print(f"[Security] Playwright cookies: {playwright_cookie_str[:200]}...")

                # Use JS cookies (real browser cookies)
                final_cookie_str = js_cookies

                # Check for __zp_stoken__
                has_stoken = '__zp_stoken__' in js_cookies
                if has_stoken:
                    for cookie_pair in js_cookies.split('; '):
                        if cookie_pair.startswith('__zp_stoken__='):
                            stoken_value = cookie_pair.split('=', 1)[1]
                            print(f"[Security] Got __zp_stoken__: {stoken_value[:20]}...")
                            break
                else:
                    print("[Security] __zp_stoken__ not found")

                print("[Security] Security check completed")

                await browser.close()

                return final_cookie_str

        except Exception as e:
            print(f"[Security] Security check failed: {e}")
            return initial_cookie

